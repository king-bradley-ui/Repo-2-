/*Teorema di Weierstrass*/
(
$$Sia $A \subset \mathbb{R}$ un compatto di $\mathbb{R}$, cio\`e un insieme chiuso e limitato. Sia $f\colon A \to \mathbb{R}$ una funzione continua. Allora $f$ ammette minimo e massimo. In particolare $f$ \`e limitata.$$,
$$Teorema di Weierstrass$$,
$$Occupiamoci solo dell'esistenza del massimo (per il minimo si procede allo stesso modo). Sia
\[
  M = \sup_{x\in A} f(x) \in ]-\infty, +\infty] \quad\text{(esiste sempre!)}
\]
1) Costruiamo una successione $(b_n)_{n\in\mathbb{N}}$ di numeri reali tale che $b_n < M$ e $b_n \to M$. A tal fine basta porre
\[
  b_n =
  \begin{cases}
    M - \dfrac{1}{n+1} & \text{se } M < +\infty \\[4pt]
    n & \text{se } M = +\infty
  \end{cases}
\]
2) Costruiamo una successione \emph{massimizzante} per $f$, cio\`e una successione $(a_n)_{n\in\mathbb{N}}$ di elementi di $A$ tale che $f(a_n) \to M$. Possiamo procedere in questo modo. Per ogni $n \in \mathbb{N}$, dato che $b_n < M$, per la seconda propriet\`a dell'estremo superiore esiste $a_n \in A$ tale che
\[
  \underbrace{b_n}_{\uparrow M} < f(a_n) \leq M
\]
Cos\`i per il teorema dei carabinieri, si ha $f(a_n) \to M$.\\
3) La successione $(a_n)_{n\in\mathbb{N}}$ \`e una successione di elementi di $A$ e $A$ \`e compatto, perci\`o esiste una sottosuccessione $(a_{n_k})_{k\in\mathbb{N}}$ convergente a qualche elemento di $A$, cio\`e $a_{n_k} \to x_0 \in A$. Poi, essendo $f$ una funzione continua, da $a_{n_k} \to x_0 \in A$ segue che $f(a_{n_k}) \to f(x_0) \in \mathbb{R}$. Ma $(f(a_{n_k}))_{k\in\mathbb{N}}$ \`e una successione estratta da $(f(a_n))_{n\in\mathbb{N}}$ che ha per limite $M$ e quindi $f(a_{n_k}) \to M$. Perci\`o si ha
\[
  f(a_{n_k}) \to M \quad\text{e}\quad f(a_{n_k}) \to f(x_0) \quad\text{per } k \to +\infty
\]
e per l'unicit\`a del limite si conclude che $M = f(x_0)$. Allora l'estremo superiore di $f$ \`e un valore assunto dalla funzione e quindi $f$ ha massimo.$$
