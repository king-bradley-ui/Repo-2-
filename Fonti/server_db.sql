$$Ogni successione convergente \`e limitata$$,
$$Vero$$,
$$Sia $(a_n)_{n\in\mathbb{N}}$ una successione di numeri reali e supponiamo che $a_n \to \ell$ per $n \to +\infty$, dove $\ell \in \mathbb{R}$. Dobbiamo provare che si pu\`o scegliere un $C > 0$ in modo che
\[
  \forall\, n \in \mathbb{N}\colon\; |a_n| \leq C.
\]
Allora, dato che $a_n \to \ell$ per $n \to +\infty$, dalla definizione di limite segue che in corrispondenza di $\varepsilon = 1$ \`e possibile trovare $\nu \in \mathbb{N}$ tale che
\[
  \forall\, n \in \mathbb{N},\, n > \nu\colon\; |a_n - \ell| < 1.
\]
Ora, dalla disuguaglianza triangolare risulta che per ogni intero $n > \nu$,
\[
  |a_n| = |a_n - \ell + \ell| \leq |a_n - \ell| + |\ell| < 1 + |\ell|.
\]
Quindi se considero la successione dei valori assoluti dei termini della successione, la situazione \`e la seguente
\[
  |a_0|,\; |a_1|,\; |a_2|,\;\ldots,\; |a_\nu|,\;
  \underbrace{|a_{\nu+1}|,\; |a_{\nu+2}|,\;\cdots}_{\text{questi termini sono} < 1+|\ell|}.
\]
Perci\`o, posto
\[
  C = \max\{|a_0|, \ldots, |a_\nu|, 1+|\ell|\} \in \mathbb{R},
\]
si ha che
\[
  \forall\, n \in \mathbb{N}\colon\;
  \begin{cases}
    \text{se } n \leq \nu, \text{ allora } |a_n| \leq C \\
    \text{se } n > \nu, \text{ allora } |a_n| < 1+|\ell| \leq C.
  \end{cases}
\]
Quindi in ogni caso, per ogni $n \in \mathbb{N}$, $|a_n| \leq C$ e perci\`o la successione \`e limitata.$$
),


/*Permanenza delle disuguaglianze*/
(
$$Sia $A \subset \mathbb{R}$ e $x_0 \in \overline{\mathbb{R}}$ un punto di accumulazione per $A$ in $\overline{\mathbb{R}}$. Siano $f\colon A \to \mathbb{R}$ e $g\colon A \to \mathbb{R}$ due funzioni e supponiamo che
\[
  \lim_{x \to x_0} f(x) = \ell \in \overline{\mathbb{R}}
  \quad\text{e}\quad
  \lim_{x \to x_0} g(x) = m \in \overline{\mathbb{R}}.
\]
Allora
\[
  \ell > m \;\Rightarrow\; f(x) > g(x) \text{ in un intorno di } x_0.
\]$$, 
$$Permanenza delle disuguaglianze$$,
$$Per ipotesi $\ell, m \in \overline{\mathbb{R}}$ e $\ell > m$. Possiamo sicuramente scegliere due intorni $V_1$ e $V_2$, rispettivamente di $\ell$ e $m$ in modo che
\[
  V_1 \cap V_2 = \varnothing \quad\text{e}\quad V_1 \geq V_2 \text{ (sono separati)}.
\]
In altre termini si ha
\begin{equation}
  \forall\, y_1 \in V_1,\, \forall\, y_2 \in V_2\colon\quad y_1 > y_2. \label{eq:perm1}
\end{equation}
Allora, dato che per ipotesi $f(x) \to \ell$ e $g(x) \to m$ per $x \to x_0$, si deve avere
\[
  f(x) \in V_1 \text{ in un intorno di } x_0 \quad\text{e}\quad g(x) \in V_2 \text{ in un intorno di } x_0.
\]
Allora risulta che
\[
  f(x) \in V_1 \text{ e } g(x) \in V_2 \text{ simultaneamente in un intorno di } x_0
\]
e quindi, per la \eqref{eq:perm1}, che
\[
  f(x) > g(x) \text{ in un intorno di } x_0.
  \qedhere
\]$$
),

/*Prolungamento delle disuguaglianze*/
(
$$Sia $A \subset \mathbb{R}$ e $x_0 \in \overline{\mathbb{R}}$ un punto di accumulazione per $A$ in $\overline{\mathbb{R}}$. Siano $f\colon A \to \mathbb{R}$ e $g\colon A \to \mathbb{R}$ due funzioni e supponiamo che $\lim_{x \to x_0} f(x) = \ell$ e $\lim_{x \to x_0} g(x) = m$. Allora
\[
  f(x) \leq g(x) \text{ in un intorno di } x_0 \;\Rightarrow\; \ell \leq m.
\]$$,
$$Vero$$,
$$Supponiamo che $f(x) \leq g(x)$ in un intorno di $x_0$. La dimostrazione procede per assurdo. Se fosse $\ell > m$, per il teorema della permanenza delle disuguaglianze (Teorema 1.1) si avrebbe
\[
  f(x) > g(x) \text{ in un intorno di } x_0.
\]
Ma stiamo supponendo che $f(x) \leq g(x)$ in un intorno di $x_0$ e quindi (prendendo come al solito l'intersezione degli intorni) si arriverebbe a
\[
  f(x) \leq g(x) \text{ e } f(x) > g(x) \text{ simultaneamente in un intorno di } x_0.
\]
E questo produce una contraddizione, dato che un numero reale non pu\`o essere simultaneamente minore o uguale e strettamente maggiore di un altro numero reale.$$
