/*Teorema dei carabinieri*/
(
$$Sia $A \subset \mathbb{R}$ e $x_0 \in \overline{\mathbb{R}}$ un punto di accumulazione per $A$ in $\overline{\mathbb{R}}$. Siano $f$, $g$ e $h$ tre funzioni definite in $A$ a valori in $\mathbb{R}$ e supponiamo che
\begin{enumerate}[label=\alph*)]
  \item $f(x) \leq g(x) \leq h(x)$ in un intorno di $x_0$.
  \item $\lim_{x \to x_0} f(x) = \ell$ e $\lim_{x \to x_0} h(x) = \ell$ con $\ell \in \overline{\mathbb{R}}$.
\end{enumerate}
Allora $\lim_{x \to x_0} g(x) = \ell$.$$,
$$Teorema dei carabinieri (o del confronto)$$,
$$Sia $V$ un intorno di $\ell$. Si deve provare che $g(x) \in V$ in un intorno di $x_0$. Osserviamo preliminarmente che, essendo $V$ un intervallo di $\overline{\mathbb{R}}$, vale che
\begin{equation}
  y_1, y_2 \in V \text{ e } y_1 \leq y_2 \;\Rightarrow\; [y_1,y_2] \subset V. \label{eq:car2}
\end{equation}
Iniziamo ora la dimostrazione. Dall'ipotesi b) si ha
\[
  f(x) \in V \text{ in un intorno di } x_0 \quad\text{e}\quad h(x) \in V \text{ in un intorno di } x_0.
\]
Inoltre, per l'ipotesi a), si ha anche che
\[
  f(x) \leq g(x) \leq h(x) \text{ in un intorno di } x_0.
\]
Perci\`o, possiamo affermare che
\[
  \begin{cases}
    f(x) \in V \\
    h(x) \in V \\
    f(x) \leq g(x) \leq h(x)
  \end{cases}
  \quad\text{sono simultaneamente vere in un intorno di } x_0.
\]
Allora, ricordando la \eqref{eq:car2}, si pu\`o concludere che $g(x) \in V$ in un intorno di $x_0$.$$
),

/*Teorema degli Zeri di Bolzano*/
(
$$Sia $I$ un intervallo di $\mathbb{R}$ e $f\colon I \to \mathbb{R}$ continua. Siano $a, b \in I$ con $a < b$ e tali che $f(a)f(b) < 0$ (cio\`e $f(a)$ e $f(b)$ hanno segni discordi). Allora esiste $x_0 \in\; ]a,b[$ tale che $f(x_0) = 0$.$$,
$$Teorema degli Zeri di Bolzano$$,
$$Utilizzeremo un metodo iterativo chiamato \emph{metodo di bisezione} per produrre una successione di intervalli chiusi e limitati ciascuno contenuto nel precedente. Poniamo $I_0 = [a_0, b_0]$, dove $a_0 = a$ e $b_0 = b$. Controlliamo il punto medio dell'intervallo $c_0 = (a_0+b_0)/2$. Se $f(c_0) = 0$ abbiamo finito (perch\'e abbiamo trovato un punto in $]a,b[$ in cui la funzione si annulla). Altrimenti se $f(c_0) \neq 0$, essendo $f(a_0)$ e $f(b_0)$ con segni discordi, $f(c_0)$ sar\`a discorde con uno di essi. Scegliamo tra i sottointervalli $[a_0, c_0]$ e $[c_0, b_0]$ quello ai cui estremi la funzione ha segno discorde e chiamiamolo $I_1$. Poniamo cio\`e
\[
  I_1 = [a_1, b_1], \text{ dove }
  \begin{cases}
    a_1 = a_0,\; b_1 = c_0 & \text{se } f(a_0)f(c_0) < 0 \\
    a_1 = c_0,\; b_1 = b_0 & \text{se } f(c_0)f(b_0) < 0.
  \end{cases}
\]
Controlliamo il punto medio di $I_1$: $c_1 = (a_1+b_1)/2$. Se $f(c_1) = 0$ abbiamo finito. Altrimenti se $f(c_1) \neq 0$ scegliamo tra i sottointervalli $[a_1, c_1]$ e $[c_1, b_1]$ quello ai cui estremi la funzione ha valore di segno discorde e chiamiamolo $I_2$, cio\`e poniamo
\[
  I_2 = [a_2, b_2] \text{ dove }
  \begin{cases}
    a_2 = a_1,\; b_2 = c_1 & \text{se } f(a_1)f(c_1) < 0 \\
    a_2 = c_1,\; b_2 = b_1 & \text{se } f(c_1)f(b_1) < 0.
  \end{cases}
\]
Al passo $n$-esimo, se abbiamo definito $I_n = [a_n, b_n]$, controlliamo il punto medio di $I_n$: $c_n = (a_n + b_n)/2$. Se $f(c_n) = 0$ abbiamo finito. Altrimenti se $f(c_n) \neq 0$, definiamo
\[
  I_{n+1} = [a_{n+1}, b_{n+1}] \text{ dove }
  \begin{cases}
    a_{n+1} = a_n,\; b_{n+1} = c_n & \text{se } f(a_n)f(c_n) < 0 \\
    a_{n+1} = c_n,\; b_{n+1} = b_n & \text{se } f(c_n)f(b_n) < 0.
  \end{cases}
\]
\hrule\vspace{4pt}
In questo modo il procedimento termina dopo un certo numero di iterazioni, oppure continua indefinitamente e definisce una successione di intervalli chiusi e limitati $(I_n)_{n\in\mathbb{N}}$, $I_n = [a_n, b_n]$, con $I_0 = [a,b]$ e
\begin{equation}
  \forall\, n \in \mathbb{N}\colon\;
  \begin{cases}
    \underbrace{a_n \leq a_{n+1} \leq b_{n+1} \leq b_n}_{I_n} \\[4pt]
    b_{n+1} - a_{n+1} = \dfrac{b_n - a_n}{2} \\[4pt]
    f(a_n)f(b_n) < 0.
  \end{cases} \label{eq:bis1}
\end{equation}
Allora dalla prima delle \eqref{eq:bis1} si vede che la successione $(a_n)_{n\in\mathbb{N}}$ \`e crescente e la successione $(b_n)_{n\in\mathbb{N}}$ \`e decrescente. Inoltre sono entrambe limitate (perch\'e $a \leq a_n \leq b_n \leq b$). Perci\`o per il teorema sui limiti delle funzioni monotone sono entrambe convergenti. In particolare
\[
  a_n \to x_0 := \sup_{n\in\mathbb{N}} a_n \in [a,b]
  \quad\text{e}\quad
  b_n \to x_1 := \inf_{n\in\mathbb{N}} b_n \in [a,b].
\]
Poi, dalla seconda delle \eqref{eq:bis1} si ha
\begin{equation}
  b_n - a_n = \frac{b_{n-1}-a_{n-1}}{2} = \frac{b_{n-2}-a_{n-2}}{2^2}
  = \cdots = \frac{b-a}{2^n} \to 0. \label{eq:bis2}
\end{equation}
Ma d'altra parte $b_n - a_n \to x_1 - x_0$ e quindi $x_0 = x_1$, cio\`e le successioni $(a_n)_{n\in\mathbb{N}}$ e $(b_n)_{n\in\mathbb{N}}$ tendono allo stesso limite $x_0 = \sup_{n\in\mathbb{N}} a_n = \inf_{n\in\mathbb{N}} b_n$. Allora
\begin{equation}
  a_n \leq x_0 \leq b_n
  \quad\text{e}\quad
  \begin{cases}
    a_n \to x_0 \\
    b_n \to x_0.
  \end{cases} \label{eq:bis3}
\end{equation}
Pertanto, per la continuit\`a di $f$ (teorema ponte) si ha
\[
  \begin{cases}
    f(a_n) \to f(x_0) \\
    f(b_n) \to f(x_0)
  \end{cases}
  \;\Rightarrow\;
  \underbrace{f(a_n)f(b_n)}_{<0} \to f(x_0)^2
  \;\xRightarrow{\text{teo di prolungamento disuguaglianze}}\;
  f(x_0)^2 \leq 0 \;\Rightarrow\; f(x_0) = 0,
\]
e il teorema \`e dimostrato.$$
),

/*Teorema dei valori Intermedi di Bolzano, 1a forma*/
(
$$Sia $f\colon I \to \mathbb{R}$ una funzione continua definita in un intervallo $I$ (qualunque: chiuso, aperto, semiaperto, limitato o illimitato) di $\mathbb{R}$. Siano $a, b \in I$ con $f(a) < f(b)$. Allora $[f(a), f(b)] \subset f(I)$. Inoltre posto
\[
  m = \inf_I f \quad\text{e}\quad M = \sup_I f,
\]
risulta $]m, M[ \;\subset f(I)$, cio\`e $\forall\, y \in\; ]m,M[$ esiste $x \in I$ tale che $f(x) = y$.$$,
$$Teorema dei valori Intermedi di Bolzano, prima forma$$,
$$Sia $y \in\; ]f(a), f(b)[$ e definiamo $g\colon I \to \mathbb{R}$ con $g(x) = f(x) - y$. Evidentemente $g(a) = f(a) - y < 0$ e $g(b) = f(b) - y > 0$ e quindi $g(a)g(b) < 0$. Perci\`o, per il teorema degli zeri se $a < b$ esiste $x \in\; ]a,b[$ tale che $g(x) = 0$, mentre se $b < a$, esiste $x \in\; ]b,a[$ tale che $g(x) = 0$. In ogni caso esiste $x \in I$ tale che $f(x) - y = g(x) = 0$ e quindi $f(x) = y$. Proviamo la seconda parte. Sia $y \in \mathbb{R}$ con $m < y < M$. Allora per le propriet\`a dell'estremo inferiore e superiore esistono $a, b \in I$ tali che
\[
  m \leq f(a) < y < f(b) \leq M.
\]
Per quanto provato prima, si ha $y \in f(I)$ e quindi esiste $x \in I$ tale che $f(x) = y$.$$
